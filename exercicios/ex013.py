# DESAFIO 13 - Faça um algorítimo que leia o salário de um funcionario e mostre seu novo salario com 15% de aumento#
salario = float(input(f"Qual o salário do funcionário? R$ "))
novosalario = salario*1.15
print(f"Um funcionário que ganhava R$ {salario}, com 15% de aumento, passa a receber R$ {novosalario}")